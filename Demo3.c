// Program
#include<stdio.h>
void main()
{
    int a=100;
    printf("value : %d\n",a);
}
/*
     Format specifier
     -----------------
     %d = integer
     %f = float
     %lf = double
     %i = short
     %c = character
     %ld = long
     %u = unsigned integer
     %lu = long unsigned integer
     %s = string
     %Ld = long double
     %p = address in hexadecimal
*/